export type LabelDisplayMode = 'name-only' | 'coords-only' | 'both' | 'global'
export type VisibilityMode = 'function-labels' | 'points-only' | 'point-labels' | 'labels-only'
export type ShapeRenderMode = 'fill' | 'stroke' | 'both'

export interface GraphPoint {
  x: number
  y: number
  label: string
  latexLabel?: string // LaTeX formatted label for rendering
  symbolicX?: string // Symbolic form of x (e.g., "1/2", "sqrt(2)")
  symbolicY?: string // Symbolic form of y (e.g., "sqrt(15)")
  color?: string // Optional color for the point
  isAutoGenerated?: boolean // True if auto-generated from click detection
  expressionId?: string // If this point is from an expression, the expression ID
  labelOffset?: { x: number; y: number } // Custom label offset in pixels (screen coordinates)
  labelQuadrant?: number // Preferred quadrant (0-3) for label positioning
  customLabel?: string // User-edited label text (e.g., "P", "Q")
  customFullLabel?: string // Complete custom label (overrides all formatting, can include LaTeX)
  hideLabel?: boolean // If true, label is hidden but point remains visible
  labelDisplayMode?: LabelDisplayMode // Individual override for label display mode
}

interface DrawingStroke {
  id: string
  points: Array<{ x: number; y: number }> // Graph coordinates (changed from screen)
  color: string
  width: number
  tool: 'pen' | 'highlighter' | 'eraser'
  selected?: boolean
}

export interface TextObject {
  id: string
  text: string
  htmlContent?: string // Rich text HTML content (if present, use this instead of text)
  x: number // Graph x coordinate
  y: number // Graph y coordinate
  fontSize: number
  color: string
  selected?: boolean
  maxWidth?: number // Maximum width for text wrapping
}

// Geometry object types
export type GeometryObjectType = 'point' | 'segment' | 'line' | 'ray' | 'circle' | 'polygon'

// Detailed geometry tool types with subtypes
export type GeometryToolType =
  // Points
  | 'point-fixed'        // 고정점
  | 'point-slider'       // 끌개
  | 'point-midpoint'     // 중점
  // Lines
  | 'line-infinite'      // 직선
  | 'line-segment'       // 선분
  | 'line-ray'           // 반직선
  // Circles
  | 'circle-center-radius'   // 중심-반지름
  | 'circle-three-points'    // 세 점 지나는 원
  | 'circle-diameter'        // 지름으로 정의
  // Polygons
  | 'polygon-regular'        // 정다각형
  | 'polygon-parallelogram'  // 평행사변형
  | 'polygon-rhombus'        // 마름모
  | 'polygon-rectangle'      // 직사각형
  | 'polygon-square'         // 정사각형
  | 'polygon-kite'           // 연
  | 'polygon-right-triangle' // 직각삼각형
  // None
  | 'none'
  | 'select'

export interface GeometryPoint {
  x: number
  y: number
}

export interface GeometryObject {
  id: string
  type: GeometryObjectType
  subType?: GeometryToolType  // Detailed subtype
  label?: string
  color: string
  strokeWidth: number
  fillColor?: string
  visible: boolean
  selected: boolean
  scale?: number            // Scale factor for maintaining constant visual size

  // Dependency system
  dependencies?: string[]   // IDs of objects this object depends on
  dependents?: string[]     // IDs of objects that depend on this object

  // Type-specific properties
  points?: GeometryPoint[]  // For point, segment, line, ray, polygon

  // Slider-specific (for point-slider)
  sliderConfig?: {
    min: number
    max: number
    step: number
    value: number
    axis: 'x' | 'y'         // Direction of slider movement
  }

  // Point constraint (for points on lines, circles, polygons)
  constraint?: {
    objectId: string        // ID of the object this point is constrained to
    type: 'line' | 'circle' | 'polygon'
    // For lines: t parameter (0-1 for segments, any value for infinite lines/rays)
    // For circles: angle in radians
    // For polygons: { edgeIndex, t } - which edge and position on that edge
    param?: number | { edgeIndex: number; t: number }
  }

  // Midpoint/division point ratio (for point-midpoint)
  ratio?: {
    m: number               // First ratio value
    n: number               // Second ratio value
  }

  // Line-specific
  linePoints?: {
    point1Id: string        // Start point ID
    point2Id: string        // End/direction point ID
  }

  // Circle-specific
  circleConfig?: {
    centerId?: string           // Center point ID
    radiusPointId?: string      // Radius-defining point ID
    point1Id?: string           // Three-point circle: first point
    point2Id?: string           // Three-point/diameter circle: second point
    point3Id?: string           // Three-point circle: third point
  }
  center?: GeometryPoint    // For circle
  radius?: number           // For circle
  point1?: GeometryPoint    // For line/ray (starting point)
  point2?: GeometryPoint    // For line/ray (direction point)

  // Polygon-specific (for polygon-regular)
  sides?: number            // Number of sides for regular polygons
}

