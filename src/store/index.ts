/**
 * Try to express number as a simple fraction
 */
function toSimpleFraction(num: number, maxDenominator: number = 12): string | null {
  const sign = num < 0 ? '-' : ''
  const absNum = Math.abs(num)

  // If it's an integer, don't use fraction
  if (Math.abs(absNum - Math.round(absNum)) < 1e-14) {
    return null
  }

  // Try to find a simple fraction (only common denominators)
  for (let denominator = 2; denominator <= maxDenominator; denominator++) {
    const numerator = Math.round(absNum * denominator)
    const fraction = numerator / denominator

    if (Math.abs(fraction - absNum) < 1e-14) {
      // Simplify by finding GCD
      const gcd = (a: number, b: number): number => b === 0 ? a : gcd(b, a % b)
      const divisor = gcd(numerator, denominator)
      const simplifiedNum = numerator / divisor
      const simplifiedDen = denominator / divisor

      if (simplifiedDen === 1) {
        return null // It's actually an integer
      }

      return `${sign}${simplifiedNum}/${simplifiedDen}`
    }
  }

  return null
}

/**
 * Try to recognize special values like π, √2, √3, etc.
 */
function toSymbolicForm(num: number): string | null {
  const absNum = Math.abs(num)
  const sign = num < 0 ? '-' : ''

  // Check for π
  if (Math.abs(absNum - Math.PI) < 1e-10) {
    return `${sign}π`
  }
  if (Math.abs(absNum - 2 * Math.PI) < 1e-10) {
    return `${sign}2π`
  }
  if (Math.abs(absNum - Math.PI / 2) < 1e-10) {
    return `${sign}π/2`
  }
  if (Math.abs(absNum - Math.PI / 3) < 1e-10) {
    return `${sign}π/3`
  }
  if (Math.abs(absNum - Math.PI / 4) < 1e-10) {
    return `${sign}π/4`
  }
  if (Math.abs(absNum - Math.PI / 6) < 1e-10) {
    return `${sign}π/6`
  }

  // Check for common square roots
  const commonRoots = [2, 3, 5, 6, 7, 10]
  for (const n of commonRoots) {
    if (Math.abs(absNum - Math.sqrt(n)) < 1e-10) {
      return `${sign}√${n}`
    }
    // Check for multiples of square roots
    for (let mult = 2; mult <= 5; mult++) {
      if (Math.abs(absNum - mult * Math.sqrt(n)) < 1e-10) {
        return `${sign}${mult}√${n}`
      }
    }
    // Check for fractions of square roots
    for (let denom = 2; denom <= 4; denom++) {
      if (Math.abs(absNum - Math.sqrt(n) / denom) < 1e-10) {
        return `${sign}√${n}/${denom}`
      }
    }
  }

  // Check for e
  if (Math.abs(absNum - Math.E) < 1e-10) {
    return `${sign}e`
  }

  return null
}

/**
 * Snap a value to special mathematical values if it's close enough
 * Returns the exact special value if close, otherwise returns the original value
 */
function snapToSpecialValue(num: number): number {
  console.log('snapToSpecialValue input:', num)
  const tolerance = 1e-3  // Tolerance for snapping to special values
  const absNum = Math.abs(num)
  const sign = num < 0 ? -1 : 1

  // Check for zero
  if (Math.abs(num) < tolerance) {
    console.log('  Snapped to 0')
    return 0
  }

  // Check for integers (within tolerance)
  const nearestInt = Math.round(num)
  if (Math.abs(num - nearestInt) < tolerance) {
    console.log(`  Snapped to integer: ${nearestInt}`)
    return nearestInt
  }

  // Check for integer multiples of π
  for (let mult = 1; mult <= 10; mult++) {
    if (Math.abs(absNum - mult * Math.PI) < tolerance) {
      console.log(`  Snapped to ${sign * mult}π`)
      return sign * mult * Math.PI
    }
  }

  // Check common fractions of π
  const piDenominators = [2, 3, 4, 6, 8, 12]
  for (const denom of piDenominators) {
    for (let numer = 1; numer < denom * 4; numer++) {
      const value = (numer * Math.PI) / denom
      if (Math.abs(absNum - value) < tolerance) {
        return sign * value
      }
    }
  }

  // Check for common square roots and their multiples
  const commonRoots = [2, 3, 5, 6, 7, 8, 10, 11, 12, 13, 14, 15]
  for (const n of commonRoots) {
    const sqrtN = Math.sqrt(n)

    // Check for √n
    if (Math.abs(absNum - sqrtN) < tolerance) {
      console.log(`  Snapped to √${n}`)
      return sign * sqrtN
    }

    // Check for multiples: 2√n, 3√n, etc.
    for (let mult = 2; mult <= 10; mult++) {
      if (Math.abs(absNum - mult * sqrtN) < tolerance) {
        console.log(`  Snapped to ${mult}√${n}`)
        return sign * mult * sqrtN
      }
    }

    // Check for fractions: √n/2, √n/3, etc.
    for (let denom = 2; denom <= 10; denom++) {
      if (Math.abs(absNum - sqrtN / denom) < tolerance) {
        console.log(`  Snapped to √${n}/${denom}`)
        return sign * sqrtN / denom
      }
    }
  }

  // Check for e
  if (Math.abs(absNum - Math.E) < tolerance) {
    return sign * Math.E
  }

  // Check for simple fractions
  for (let denom = 2; denom <= 20; denom++) {
    for (let numer = 1; numer < denom * 3; numer++) {
      const value = numer / denom
      if (Math.abs(absNum - value) < tolerance) {
        return sign * value
      }
    }
  }

  // Return original value if no special value is close
  console.log('  No snap, returning original:', num)
  return num
}

/**
 * Format coordinate with appropriate precision
 */
function formatCoord(num: number): string {
  // If very close to an integer, show as integer
  if (Math.abs(num - Math.round(num)) < 1e-10) {
    return Math.round(num).toString()
  }

  // Try symbolic form first
  const symbolic = toSymbolicForm(num)
  if (symbolic) {
    return symbolic
  }

  // Try simple fraction
  const fraction = toSimpleFraction(num)
  if (fraction) {
    return fraction
  }

  // Otherwise show with appropriate decimal places (max 2 decimal places)
  return num.toFixed(2)
}

/**
 * Format coordinate as LaTeX
 */
export function formatCoordLatex(num: number): string {
  console.log('formatCoordLatex input:', num)

  const absNum = Math.abs(num)
  const sign = num < 0 ? '-' : ''
  // Very strict tolerance - only match if within ~14 decimal places
  const tolerance = 1e-14

  // If very close to an integer, show as integer
  if (Math.abs(num - Math.round(num)) < tolerance) {
    console.log('  Formatted as integer:', Math.round(num))
    return Math.round(num).toString()
  }

  // Check for π multiples and fractions - very strict
  const piTolerance = tolerance

  // Check integer multiples of π first (0π, 1π, 2π, 3π, etc.)
  for (let mult = 1; mult <= 10; mult++) {
    if (Math.abs(absNum - mult * Math.PI) < piTolerance) {
      if (mult === 1) {
        return `${sign}\\pi`
      }
      return `${sign}${mult}\\pi`
    }
  }

  // Check common fractions of π
  const piDenominators = [2, 3, 4, 6, 8, 12]
  for (const denom of piDenominators) {
    for (let numer = 1; numer < denom * 4; numer++) {
      const value = (numer * Math.PI) / denom
      if (Math.abs(absNum - value) < piTolerance) {
        // Simplify the fraction
        const gcd = (a: number, b: number): number => b === 0 ? a : gcd(b, a % b)
        const divisor = gcd(numer, denom)
        const simplifiedNumer = numer / divisor
        const simplifiedDenom = denom / divisor

        if (simplifiedDenom === 1) {
          if (simplifiedNumer === 1) {
            return `${sign}\\pi`
          }
          return `${sign}${simplifiedNumer}\\pi`
        }
        if (simplifiedNumer === 1) {
          return `${sign}\\frac{\\pi}{${simplifiedDenom}}`
        }
        return `${sign}\\frac{${simplifiedNumer}\\pi}{${simplifiedDenom}}`
      }
    }
  }

  // Check for common square roots
  const commonRoots = [2, 3, 5, 6, 7, 8, 10, 11, 12, 13, 14, 15]
  for (const n of commonRoots) {
    const sqrtN = Math.sqrt(n)

    // Check for √n
    if (Math.abs(absNum - sqrtN) < tolerance) {
      return `${sign}\\sqrt{${n}}`
    }

    // Check for multiples: 2√n, 3√n, etc.
    for (let mult = 2; mult <= 10; mult++) {
      if (Math.abs(absNum - mult * sqrtN) < tolerance) {
        return `${sign}${mult}\\sqrt{${n}}`
      }
    }

    // Check for fractions: √n/2, √n/3, etc.
    for (let denom = 2; denom <= 10; denom++) {
      if (Math.abs(absNum - sqrtN / denom) < tolerance) {
        return `${sign}\\frac{\\sqrt{${n}}}{${denom}}`
      }
    }
  }

  // Check for e
  if (Math.abs(absNum - Math.E) < 1e-10) {
    return `${sign}e`
  }

  // Try simple fraction
  const fraction = toSimpleFraction(num)
  if (fraction) {
    // Convert "a/b" to "\frac{a}{b}"
    const match = fraction.match(/^(-?)(\d+)\/(\d+)$/)
    if (match) {
      const [, minusSign, numerator, denominator] = match
      return `${minusSign}\\frac{${numerator}}{${denominator}}`
    }
  }

  // Otherwise show with appropriate decimal places (max 2 decimal places)
  return num.toFixed(2)
}

/**
 * Save current state to history
 */
// Debounce timer for saveHistory
let saveHistoryTimer: ReturnType<typeof setTimeout> | null = null
let pendingState: AppState | null = null

function saveHistoryImmediate(state: AppState) {
  // Remove any history after current index (when undoing and then making new changes)
  state.history = state.history.slice(0, state.historyIndex + 1)

  // Deep copy via JSON to safely clone Immer draft/Proxy objects
  // Only clone current page data (not all pages' content) for memory efficiency
  const clone = (obj: any) => JSON.parse(JSON.stringify(obj))
  const snapshot: HistoryState = {
    expressions: clone(state.expressions),
    graphPoints: clone(state.graphPoints),
    images: clone(state.images),
    drawings: clone(state.drawings),
    textObjects: clone(state.textObjects),
    geometryObjects: clone(state.geometryObjects),
    view: clone(state.view),
    pages: state.pages.map(p => ({ id: p.id, name: p.name, thumbnail: p.thumbnail })) as any,
    currentPageIndex: state.currentPageIndex,
  }

  // Add to history
  state.history.push(snapshot)
  state.historyIndex = state.history.length - 1

  // Limit history to 30 steps to reduce memory usage
  const MAX_HISTORY = 30
  if (state.history.length > MAX_HISTORY) {
    state.history.shift()
    state.historyIndex--
  }
}

function saveHistory(state: AppState) {
  // For high-frequency operations, debounce to avoid excessive snapshots
  saveHistoryImmediate(state)
}

